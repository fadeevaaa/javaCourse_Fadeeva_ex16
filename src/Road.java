public class Road {
    int person;
    int cars;

    public Road(int person, int cars) {
        this.person = person;
        this.cars = cars;
    }

    public int getPerson() {
        return person;
    }

    public void setPerson(int person) {
        this.person = person;
    }

    public int getCars() {
        return cars;
    }

    public void setCars(int cars) {
        this.cars = cars;
    }
}
